/*
 * 程序名称：C语言字面量后缀重要性对比演示
 * 编译命令：gcc -Wall -std=c99 suffix_importance.c -o suffix_importance
 *
 * 核心目的：展示在数字后加上 L, LL, UL, f 等后缀的必要性，
 * 以及不加后缀时可能导致的溢出、精度丢失和未定义行为。
 */

#include <stdio.h>

int main()
{
    printf("=======================================================\n");
    printf("      C语言字面量后缀（L, LL, f 等）重要性对比演示     \n");
    printf("=======================================================\n\n");

    /* =========================================================================
     * 场景 1：大数相乘导致的“整数溢出” (Integer Overflow)
     * =========================================================================
     * 目标：计算 100万 乘以 100万，理论正确结果应该是 1万亿 (1,000,000,000,000)。
     * 陷阱：100万默认是 int 类型，两个 int 相乘，结果依然是 int。
     *       但 1万亿 远超出了 32位 int 的最大值 (约21.4亿)，导致内存装不下而溢出。
     */
    printf("【场景 1：大数相乘导致的整数溢出】\n");
    printf("目标计算: 1,000,000 * 1,000,000 (理论结果应为 1万亿)\n");

    // ❌ 错误示范：没有加 LL 后缀
    // 编译器看到 1000000，认为它是 int。int * int = int。
    // 乘积在 int 范围内溢出，变成了一个错误的负数。
    // 然后，编译器把这个“已经错误的负数”赋值给 long long 类型的 result_wrong。
    long long result_wrong = 1000000 * 1000000;

    // ✅ 正确示范：加上 LL 后缀
    // 明确告诉编译器：这是 long long 类型的数字。
    // long long * long long = long long，空间足够，计算正确。
    long long result_right = 1000000LL * 1000000LL;

    printf("❌ 没加后缀 (1000000 * 1000000)   -> 结果: %lld (溢出变成了负数!)\n", result_wrong);
    printf("✅ 加了后缀 (1000000LL * 1000000LL) -> 结果: %lld (计算正确)\n\n", result_right);

    /* =========================================================================
     * 场景 2：浮点数赋值时的“隐式转换”与精度截断
     * =========================================================================
     * 目标：将一个高精度的小数赋值给单精度浮点数 float。
     * 陷阱：C语言规定，所有带小数点的数字（如3.14），默认一律是 double（双精度，8字节）。
     *       把 8字节 的 double 塞进 4字节 的 float，会发生精度截断。
     */
    printf("【场景 2：浮点数赋值时的隐式转换】\n");
    printf("目标操作: 将高精度小数赋值给 float 变量\n");

    // ❌ 错误示范：没有加 f 后缀
    // 3.14159265358979323846 默认是 double (8字节)。
    // 编译器必须先把它强制截断成 4字节的 float，再赋值给 f_wrong。
    // 【注意】：编译时如果加了 -Wall，这里会报出警告 (conversion from 'double' to 'float')！
    float f_wrong = 3.14159265358979323846;

    // ✅ 正确示范：加上 f 后缀
    // 明确告诉编译器：这个数字本身就是 float (4字节)。
    // 直接完美匹配赋值，没有类型转换，没有警告，没有额外的 CPU 性能损耗。
    float f_right = 3.14159265358979323846f;

    // 虽然在这个简单例子中，打印出来的结果看起来一样（因为float本身精度就有限），
    // 但背后的“转换过程”和“编译器警告”是截然不同的。
    printf("❌ 没加后缀 (默认 double 转 float) -> 触发编译器警告，浪费CPU做类型转换\n");
    printf("✅ 加了后缀 (直接就是 float)       -> 完美匹配，零警告，性能最优\n");
    printf("(两者打印值均为: %.5f，但底层行为不同)\n\n", f_right);

    /* =========================================================================
     * 场景 3：位运算左移越界 (Undefined Behavior)
     * =========================================================================
     * 目标：将数字 1 左移 40 位 (相当于 1 乘以 2的40次方)。
     * 陷阱：数字 1 默认是 int (通常是 32位)。
     *       把 32位 的容器左移 40 位，位都移没了，这在C语言中属于“未定义行为”。
     */
    printf("【场景 3：位运算左移越界（未定义行为）】\n");
    printf("目标计算: 1 左移 40 位 (理论结果应为 1,099,511,627,776)\n");

    // ❌ 错误示范：没有加 LL 后缀
    // 1 是 32位的 int。左移 40 位超出了 32位 的物理极限。
    // 结果通常是 0，或者一些不可预知的垃圾值（取决于编译器和系统）。
    long long shift_wrong = 1 << 40;

    // ✅ 正确示范：加上 LL 后缀
    // 1LL 是 64位的 long long。64位 的容器左移 40 位，空间绰绰有余，结果正确。
    long long shift_right = 1LL << 40;

    printf("❌ 没加后缀 (1 << 40)   -> 结果: %lld (32位int移40位，越界变成0)\n", shift_wrong);
    printf("✅ 加了后缀 (1LL << 40) -> 结果: %lld (64位移40位，计算正确)\n\n", shift_right);

    printf("=======================================================\n");
    printf(" 总结：后缀不是给 printf 看的，而是给“编译器”看的！  \n");
    printf(" 养成加后缀的好习惯，能帮你避开 90%% 的底层计算 Bug。 \n");
    printf("=======================================================\n");

    return 0;
}